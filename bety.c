/**
 * multiply - Find the multiplied answer to two numbers
 *
 * @num1: first random number
 * @num2: second random number
 * @result: stores the multiplied number.
 *
 * Return: The result of the multiplied numbers.
 */

int multiply(int num1, int num2, long result)
{
	result = num1 * num2;
	return (result);
}
